# Write a program to print the following values 
'''
a = 12.34 b = 234.39 c = 444.34 d = 1.23 e = 34.67 as shown below: 
a =  12.34, 
b = 234.39, 
c = 444.34, 
d =   1.23, 
e =  34.67
'''

a = 12.34
b = 234.39
c = 444.34
d = 1.23
e = 34.67

print(f"a = {a:6.2f},")
print(f"b = {b:6.2f},")
print(f"c = {c:6.2f},")
print(f"d = {d:6.2f},")
print(f"e = {e:6.2f}")

# Output:
# a =  12.34,
# b = 234.39,
# c = 444.34,
# d =   1.23,
# e =  34.67

