# Is the following statement correct? print('Result = ' + 4 > 3)

print('Result = ' + str(4 > 3))

# Output:
# Result = True

# Explanation:
# The print() function is used to display the string 'Result = ' followed by the result of the comparison between 4 and 3, which is True.
# The str() function is used to convert the True value to a string before concatenating it with the string 'Result = '.
# Thus, the final output is: Result = True

