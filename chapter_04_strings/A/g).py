# Mention two ways to store a string: He said, "Let Us Python".

s = "He said, \"Let Us Python\""
print(s)

s = 'He said, "Let Us Python"'
print(s)

# Output:
# He said, "Let Us Python"
# He said, "Let Us Python"
# Explanation:
# The program defines a string s using single quotes and double quotes.
# It then prints the resulting string.
