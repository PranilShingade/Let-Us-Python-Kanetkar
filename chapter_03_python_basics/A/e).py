#Assume a suitable value for temperature of a city in Fahrenheit degrees. Write a program to convert this temperature into Centigrade degrees and print both temperatures

fahrenheit = 98.6  # Example temperature in Fahrenheit
centigrade = (fahrenheit - 32) * 5.0/9.0
print("Temperature in Fahrenheit:", fahrenheit)
print("Temperature in Centigrade:", centigrade)
# Output:
# Temperature in Fahrenheit: 98.6
# Temperature in Centigrade: 37.0
# Explanation:
# The program assumes a temperature value in Fahrenheit.
# It then converts the Fahrenheit temperature to Centigrade using the formula (Fahrenheit - 32) * 5.0/9.0.
# Finally, it prints the original Fahrenheit temperature and the converted Centigrade temperature.