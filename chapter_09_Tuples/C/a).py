# Is this a valid tuple? tpl = ('Square')

tpl = ('Square',)
print(tpl)


# Output:
# ('Square',)

# Explanation:
# In Python, a tuple is defined by enclosing elements in parentheses. However, if you want to create a tuple with a single element, you need to include a comma after the element. Without the comma, Python will interpret it as a string rather than a tuple. Therefore, the correct way to create a tuple with one element is ('Square',).


